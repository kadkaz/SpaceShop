CREATE TABLE SHIPS (
  id bigint GENERATED BY DEFAULT AS IDENTITY (START WITH 13) NOT NULL PRIMARY KEY,
  name nvarchar(255) NOT NULL
);

CREATE TABLE PASSENGERS (
  id bigint GENERATED BY DEFAULT AS IDENTITY (START WITH 13) NOT NULL PRIMARY KEY,
  name nvarchar(255) NOT NULL,
  ship_id bigint NOT NULL
);

